import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import tensorflow as tf

sns.get_dataset_names()

df=sns.load_dataset("mpg")

df.head()

# df["name"].unique()

# for i in df.columns:
#   print(df[i].value_counts)
#   print(df[i].unique())
#   print("="*100)

df.isnull().sum()

df.duplicated().sum()

df.describe()

df.info()

df["brand"] = df["name"].str.split().str[0]

df.head()

df=df.drop(columns=["name"])

df.describe()

df.shape

df["horsepower"].value_counts()

df["horsepower"] = df["horsepower"].fillna(df["horsepower"].median())

df = pd.get_dummies(df, columns=["brand", "origin"], drop_first=True)

df.info()

df = df.astype("float32")

from sklearn.preprocessing import StandardScaler
from sklearn.model_selection import train_test_split

x=df.drop(columns=["mpg"])
y=df["mpg"]

x_train,x_test,y_train,y_test=train_test_split(x,y,test_size=0.2,random_state=2)

scaler=StandardScaler()
x_train_scaled=scaler.fit_transform(x_train)
x_test_scaled=scaler.transform(x_test)



from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Dense,Dropout,BatchNormalization
from tensorflow.keras.callbacks import EarlyStopping
from tensorflow.keras import regularizers

model=Sequential()

model.add(Dense(64,activation="relu"))
# model.add(BatchNormalization())
model.add(Dropout(0.08))
model.add(Dense(34,activation="relu"))
# model.add(BatchNormalization())
model.add(Dropout(0.08))
model.add(Dense(28,activation="relu"))
# model.add(BatchNormalization())
# model.add(Dropout(0.08))
model.add(Dense(1,activation="linear"))

tf.random.set_seed(42)
np.random.seed(42)

callback=EarlyStopping(
    monitor="val_loss",
    patience=16,
    verbose=1,
    restore_best_weights=True
)

model.compile(loss="mean_squared_error",optimizer="adam",metrics=["r2_score"])

history=model.fit(x_train_scaled,y_train,epochs=100,batch_size=16,validation_split=0.2,callbacks=callback)

model.get_weights()[0][0]

from sklearn.metrics import r2_score,mean_squared_error,mean_absolute_error

y_pred=model.predict(x_test_scaled)

r2_score(y_test,y_pred)

mean_squared_error(y_test,y_pred)

mean_absolute_error(y_pred,y_test)

plt.plot(history.history["loss"])
plt.plot(history.history["val_loss"])

plt.plot(history.history["r2_score"])
plt.plot(history.history["val_r2_score"])

